// Write a dart program to print the color of the rainbow based onthe input.
// (The color of rainbow are Voilet, Indigo, Blue, Green , Yellow,
// Orange Red)
// Input : x=1
// Output: Violet
// Input : x=2
// Output: Indigo

void main() {
  int x = 1;

  if (x == 1) {
    print('Violet');
  } else if (x == 2) {
    print('Indigo');
  } else if (x == 3) {
    print('Blue');
  } else if (x == 4) {
    print('Green');
  } else if (x == 5) {
    print('Yellow');
  } else if (x == 6) {
    print('Orange');
  } else if (x == 7) {
    print('Red');
  } else {
    print('Invalid input');
  }
}
